package eu.lucaventuri.examples;

public class TestMessage {
    private String a;
    private int b;

    public TestMessage() {
    }

    public TestMessage(String a, int b) {
        this.a = a;
        this.b = b;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
